#include <iostream>

int main() {
    int n; std::cin >> n;
    // p + v + k = n
    // 1) (p - v - k) + (2*v) + (2*k) = n
    // 2) 2*(p - v - k) + (4*v) + (2*k - (p - v - k) - 2*v) = n
    // 3) 4*(p - v - k) + (4*v - 2*(p - v - k) - (2*k - (p - v - k) - 2*v)) + 2*(2*k - (p - v - k) - 2*v) = n
    // 4*(p - v - k) == 4*v - 2*(p - v - k) - (2*k - (p - v - k) - 2*v) == 2*(2*k - (p - v - k) - 2*v)  (2)
    // ==> 2*(p - v - k) == 2*k - (p - v - k) - 2*v
    //   ==> 3*(p - v - k) == 2*k - 2*v
    //     ==> 3p - 3v - 3k = 2k - 2v
    //       ==> 3p - 5k = v  (1)

    // p + (3p - 5k) + k == n
    // ==> 4p - 4k = n
    //  ==> p - k = n/4 ==> p == n/4 + k  // (3)
    // 4 * (p - v - k) == n/3  // using (2)
    // ==> p - v - k = n/12
    //  ==> p - (3p - 5k) - k = n/12 // using (1)
    //   ==> -2p + 4k = n/12
    //    ==> 2k - p = n/24
    //     ==> 2k - (n/4 + k) == n/24 // using (3)
    //      ==> 2k - n/4 - k == n/24
    //       ==> k == n/24 + n/4
    //        ==> k == (n + 6n) / 24
    //         ==> k == 7n/24  // (4)
    int k = 7 * n / 24; // (4)
    int p = n / 4 + k;  // (3)
    int v = n - k - p;
    std::cout << p << ' ' << v << ' ' << k;
}